/* ==== JavaScript template literals or back ticks ==== */



// use template literals:------------
const num1 = 100;
const num2 = 100;

const totalNum = `Total number i ${num1 + num2}.`;

console.log(totalNum);  // output: Total number i 200.

const numbers = `num1 is a ${num1} &n num2 is a ${num2}.`;

console.log(numbers); // output: num1 is a 100 &n num2 is a 100.   


